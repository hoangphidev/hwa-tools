if (!function_exists('code_to_country')) {

    /**
     * Code to country
     *
     * @param $code
     * @return string
     */
    function code_to_country($code)
    {
        $code = strtoupper($code);

        if (!empty($code) && strlen($code) == 2) {
            $countryList = get_country_list();

            if (is_array($countryList)) {
                $name = 'unknown';
                if (isset($countryList[$code])) {
                    $name = $countryList[$code];
                }
                return $name;
            } else {
                return 'unknown';
            }
        } else {
            return 'unknown';
        }
    }
}

